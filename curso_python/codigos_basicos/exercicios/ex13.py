# Python |Verificar se um elemento existe na lista

lista_convidados = ['Lucas', 'Fernado', 'Maria', 'Eduardo', 'Salomão', 'Davi','Keira']

nome = input('Digite um nome para verificar se está lista de convidado: ')

if nome in lista_convidados:
    print('Nome consta na lista de convidados.')

else:
    print('Nome não consta na lista de convidados.')

