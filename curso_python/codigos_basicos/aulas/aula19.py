# Operadores Relacionais


# Maior (>). Exemplo: 2 > 1

# Maior ou igual (>=). Exemplo: 2 >= 2

# Menor (<). Exemplos: 1 < 2

# Menor ou igual (<=). Exemplo: 2 <= 2

# Igual (==). Exemplo: "a" == "a"

# Diferente (!=). Exemplo: 'a' != 'b'

# Praticando:

print(1 > 2) # Retorna um valor booleano (False).

print(2 == 2) # Retorna um valor booleano (True).

print(25 < 89) # Retorna um valor booleano (True).

print (234 != 234) # Retorna um valor booleano (False)

print ('carro' == 'carro') # Retorna um valor booleano (True).